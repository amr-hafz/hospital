    private void filltable(){
     try {
         
         dtm.setRowCount(0);
            PreparedStatement stm=con.prepareStatement("select name,gender,number,disease,blood_type,id from patient" );
           ResultSet rs= stm.executeQuery();
           while(rs.next()){
         dtm.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            empids.add(rs.getInt(6));
           }
           table1.setModel(dtm);
        } catch (SQLException ex) {
           // Logger.getLogger(patient.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(this, "ERROR");
        }
    
    
    }
    
    
