     private void filltable(){
     try {
         
     dtm.setRowCount(0);
            PreparedStatement stm=con2.prepareStatement("select doc_id,doc_name,specialization,gender,address,phone_number from doctor" );
           ResultSet rs= stm.executeQuery();
           while(rs.next()){
                  empids.add(rs.getInt(1));
         dtm.addRow(new Object[]{rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
           
           }
           table2.setModel(dtm);
        } catch (SQLException ex) {
           // Logger.getLogger(patient.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(this, "ERROR");
        }
    
 
    
    }
    private void updatefun(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatefun
        // TODO add your handling code here:
        if(
                !docName.getText().isEmpty()&&!special.getText().isEmpty()&&!phoenumber.getText().isEmpty()){
        try {
            // TODO add your handling code here:
           //dtm.setRowCount(0); 
            PreparedStatement stm=con2.prepareStatement("update doctor  set doc_name=?,specialization=?,gender=?,address=?,phone_number=? where doc_id=? ");
            stm.setString(1, docName.getText());
            if(male.isSelected())
                stm.setString(3, "male");
            else
                stm.setString(3, "female");
            stm.setString(2, special.getText());
            stm.setString(4, address.getText());
            stm.setString(5, phoenumber.getText());
            stm.setInt(6, empids.get(table2.getSelectedRow()));
            stm.executeUpdate();
            JOptionPane.showMessageDialog(this, "Updated successfully");
            filltable();
            docName.setText(null);
            special.setText(null);
          
            phoenumber.setText(null);
            address.setText(null);
        } catch (SQLException ex) {
            Logger.getLogger(patient.class.getName()).log(Level.SEVERE, null, ex);
        }}
        else
            JOptionPane.showMessageDialog(this,"Not Saved");        
        
       
    }//GEN-LAST:event_updatefun

    
